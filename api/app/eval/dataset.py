"""Golden dataset for the RAG eval harness.

Two pieces:

* ``CORPUS`` — the notes a single synthetic user has written. This is the
  knowledge base the RAG pipeline retrieves from. Each note carries a stable
  ``slug`` so eval samples can point at the note(s) that *should* be retrieved
  without depending on autogenerated database ids.

* ``GOLDEN`` — the eval samples. Each is a question paired with a human-written
  reference answer (``ground_truth``) and the slugs of the notes that actually
  contain the answer (``relevant_slugs``). A few samples are deliberately
  *unanswerable* from the corpus (``relevant_slugs == []``): a faithful RAG
  system must decline these rather than invent an answer, and they are what the
  faithfulness metric and the failure-mode log lean on hardest.

The corpus is intentionally small and hand-checked. The point of an eval set is
that every label is trustworthy, not that it is large — a noisy 500-row set
tells you less than a clean 25-row one.
"""

from dataclasses import dataclass, field


@dataclass(frozen=True)
class CorpusNote:
    slug: str
    title: str
    content: str
    tags: list[str] = field(default_factory=list)


@dataclass(frozen=True)
class EvalSample:
    question: str
    ground_truth: str
    # Slugs of the corpus notes that contain the answer. Empty means the corpus
    # cannot answer the question — the system should say so, not hallucinate.
    relevant_slugs: list[str] = field(default_factory=list)


